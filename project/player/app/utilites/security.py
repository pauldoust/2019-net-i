import hashlib


class Security:
    ####################################################################################################################
    #                                           SECURITY MODULE
    ####################################################################################################################

    @staticmethod
    def sha1(chunk):
        """
        *****************************************
        Method create a sha1 checksum


        :param: book: bytes
        :return String

        *****************************************
        """
        hasher = hashlib.sha1()
        hasher.update(chunk)
        result = hasher.hexdigest()
        return result

    ####################################################################################################################
    #                                          END SECURITY MODULE
    ####################################################################################################################
